
    // video 
    function playVideo() {
        var video = document.getElementById("myVideo");
        video.play();
        }

        function skipVideoOnClick(secimage, myVideo) {
        
          const imageElement = document.getElementById("secimage");
          const videoElement = document.getElementById("myVideo");

            videoElement.currentTime = Math.min(videoElement.currentTime + 10, videoElement.duration);
        }  
        
        function reverseVideoOnClick(secimage, myVideo) {

          const imageElement = document.getElementById("secimage");
          const videoElement = document.getElementById("myVideo");

            videoElement.currentTime = Math.min(videoElement.currentTime - 10, videoElement.duration);
        } 
     
    function pauseVideo() {
      var video = document.getElementById("myVideo");
      video.pause();
    }

